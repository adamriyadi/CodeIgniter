		  <h1 class="page-header">"The quick brown fox <small>jumps over the lazy dog"</small></h1>
		  <h5><p> is an English-language pangram-a phrase that contains all of the letters of the English alphabet. It has been used to test typewriters and computer keyboards, and in other applications involving all of the letters in the English alphabet. Owing to its brevity and coherence, it has become widely known.</p>
		  <p>The earliest known appearance of the phrase is from The Michigan School Moderator, a journal that provided teachers with education-related news and suggestions for lessons. In an article titled "Interesting Notes" in the March 14, 1885 issue, the phrase is given as a suggestion for writing practice: "The following sentence makes a good copy for practice, as it contains every letter of the alphabet: 'A quick brown fox jumps over the lazy dog.'" Note that the phrase in this case begins with the word "A" rather than "The". Several other early sources also use this variation.

			As the use of typewriters grew in the late 19th century, the phrase began appearing in typing and stenography lesson books as a practice sentence. Early examples of publications which utilized the phrase include Illustrative Shorthand by Linda Bronson (1888),[3] How to Become Expert in Typewriting: A Complete Instructor Designed Especially for the Remington Typewriter (1890),[4] and Typewriting Instructor and Stenographer's Hand-book (1892). By the turn of the 20th century, the phrase had become widely known. In the January 10, 1903, issue of Pitman's Phonetic Journal, it is referred to as "the well known memorized typing line embracing all the letters of the alphabet".[5] Robert Baden-Powell's book Scouting for Boys (1908) uses the phrase as a practice sentence for signalling.[6]

			The first message sent on the Moscow-Washington hotline was the test phrase "THE QUICK BROWN FOX JUMPED OVER THE LAZY DOG'S BACK 1234567890".[7] Later, during testing, the Russian translators sent a message asking their American counterparts "What does it mean when your people say 'The quick brown fox jumped over the lazy dog?'"[8]

			During the 20th century, technicians tested typewriters and teleprinters with repeated lines of "THE QUICK BROWN FOX..." sentence.[9]

			Due to the ubiquitous popularity of the phrase, numerous passing references to the phrase have occurred in movies, television, books, advertising, websites, and graphic arts. One notable example is the lipogrammatic novel Ella Minnow Pea by Mark Dunn, a story about a fictional island in which the phrase is cast on a memorial held in great respect. As letters fall off, the government bans the use of those letters. The novel's text becomes increasingly more and more restricted as letters continuously fall off throughout the story.</p>
